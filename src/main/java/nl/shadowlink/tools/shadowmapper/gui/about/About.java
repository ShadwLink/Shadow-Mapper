    /** Creates new form About */
    public About(String[] values, String[] thanks) {
        this.thanks = thanks;
        this.setIconImage(java.awt.Toolkit.getDefaultToolkit().createImage("icon.png"));
        initComponents();

        //center the window
        Toolkit toolkit = Toolkit.getDefaultToolkit();
        Dimension screenSize = toolkit.getScreenSize();
        int x = (screenSize.width - this.getWidth()) / 2;
        int y = (screenSize.height - this.getHeight()) / 2;
        this.setLocation(x, y);

        setText(values);
        this.setVisible(true);
    }

    public void setText(String[] values){
        labelName.setText(values[0]);
        labelVersion.setText(values[1]);
        labelDate.setText(values[2]);
    }

    public void paintPanel(Graphics g){
        for(int i = 0; i < thanks.length; i++){
            g.drawString(thanks[i], (int)(105-thanks[i].length()*2.6), (i*10)+currentHeight);
        }
        if(currentWait >= wait){
            if(currentHeight <= thanks.length*-10){
                currentHeight = 140;
            }else{
                currentHeight -= 1;
            }
            currentWait = 0;
        }else{
            currentWait += 0.7;
        }
        updateFPS();
        this.repaint();
    }

    public void updateFPS(){
        long currentTime= (System.currentTimeMillis());

        ++fps;

        if(currentTime - previousTime >= 1000) {
            previousTime = currentTime;
            finalFPS = fps;
            wait = finalFPS/60;
            fps = 0;
        }
    }

