package nl.shadowlink.tools.shadowlib.model.model;

/**
 *
 * @author Shadow-Link
 */
public class Polygon {
    public int a;
    public int b;
    public int c;

    /**
     * Create a new polygon
     * @param a vertex of the polygon
     * @param b vertex of the polygon
     * @param c vertex of the polygon
     */
    public Polygon(int a, int b, int c){
        this.a = a;
        this.b = b;
        this.c = c;
    }

}
